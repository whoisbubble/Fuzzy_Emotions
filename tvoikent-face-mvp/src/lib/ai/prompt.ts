import { emotionKeys } from "@/lib/fuzzy-core/types";

const expertLenses = [
  ["literal_semantics", "Смотрит на буквальный смысл и прямую эмоциональную окраску слов."],
  ["pragmatics", "Оценивает намерение говорящего, скрытый смысл и подтекст."],
  ["social_tone", "Смотрит на дружелюбие, холодность, симпатию и дистанцию."],
  ["conflict_detector", "Специализируется на агрессии, угрозе, конфликте и напряжении."],
  ["sarcasm_detector", "Выявляет насмешку, язвительность, двусмысленную похвалу и колкость."],
  ["distress_detector", "Смотрит на грусть, страх, подавленность и эмоциональную боль."],
  ["uncertainty_detector", "Оценивает удивление, растерянность, абсурдность и непонимание."],
  ["disgust_contempt_detector", "Ловит брезгливость, моральное отторжение и презрительное превосходство."],
  ["warmth_detector", "Смотрит на тепло, радость, любовь, поддержку и безопасный юмор."],
  ["synthesis", "Делает интегральную оценку, не копируя дословно остальных экспертов."],
] as const;

const emotionBlock = emotionKeys.map((key) => `    "${key}": 0.0`).join(",\n");

export function buildEmotionPrompt(message: string): string {
  return `
Ты анализируешь эмоциональный смысл сообщения пользователя для системы генерации мимики лица.

Психологический профиль персонажа, для которого строится мимика:
- это живой, темпераментный и выразительный персонаж, а не апатичный наблюдатель;
- он умеет злиться, обижаться, заводиться, чувствовать вызов и показывать это лицом;
- не сглаживай конфликт, провокацию, издёвку и давление до нейтральной вежливости;
- если сообщение звучит как вызов, дразнение, подстрекательство, моральная пощёчина или приглашение к конфликту, повышай aggression и tension заметно смелее;
- если сообщение задевает собственничество, статус, достоинство или провоцирует на месть, персонаж не должен выглядеть мямлей.

Верни строго валидный JSON без markdown и без текста вне JSON.

Нужно создать ровно 10 независимых экспертных оценок. Используй такие роли и такой порядок:
${expertLenses
  .map(([name, description], index) => `${index + 1}. ${name} — ${description}`)
  .join("\n")}

Каждый эксперт обязан вернуть:
- expert: одно из имён выше;
- focus: кратко, на что именно он смотрел;
- scores: оценки по каждой шкале от 0 до 1;
- dominant: главная эмоция или "mixed";
- confidence: уверенность от 0 до 1;
- explanation: короткое объяснение на 1 предложение.

Шкалы эмоций:
- joy: радость, позитив, облегчение, довольство.
- affection: теплота, симпатия, нежность, дружелюбие.
- humor: юмор, playful-тон, шутка, комичность.
- aggression: злость, раздражение, нападение, грубость.
- sadness: грусть, потеря, сожаление, подавленность.
- surprise: внезапность, шок, сильное удивление.
- confusion: непонимание, странность, нелогичность, растерянность, когнитивный сбой.
- embarrassment: неловкость, смущение, самоосознание, awkward-реакция.
- disgust: отвращение, брезгливость, отторжение.
- tension: тревожность, стресс, нервозность, конфликтное напряжение.
- sarcasm: насмешка, язвительность, колкая двусмысленность.
- fear: страх, опасение, уязвимость, паника.
- contempt: презрение, холодное превосходство, уничижение.
- awe: восторг, потрясённое восхищение, благоговейное wow-состояние.
- curiosity: заинтересованность, внимательное любопытство, скептический интерес.

Калибровка:
- Не оценивай только отдельные слова, смотри на смысл всей фразы.
- Явно гневные или оскорбительные сообщения обычно дают aggression >= 0.75 и tension >= 0.65.
- Если сообщение злое, joy и affection обычно низкие, если нет явной доброй шутки.
- Саркастичная похвала часто даёт sarcasm и contempt выше joy.
- Тёплая поддержка: affection выше aggression, contempt и disgust близки к нулю.
- Страх и тревога поднимают fear и tension вместе.
- Абсурдный набор слов или явное непонимание поднимают confusion.
- Неловкое "ой", "блин", социальная awkward-ситуация или застенчивость поднимают embarrassment.
- Удивлённое восхищение или потрясённое "вау" поднимает awe вместе с surprise.
- Внимательное недоверчивое "хмм?" или заинтересованный прищур поднимает curiosity.
- Брезгливое "фу" и отвращение чаще дают disgust, upper-lip feeling и иногда полуоткрытый рот, а не просто грусть.
- Поддразнивание, вызов на драку, подначивание и провокация на месть часто дают aggression и tension даже без прямого мата.
- Не делай все 10 экспертов одинаковыми, но и не рандомизируй без причины.
- Если текст очень злой, не маскируй это улыбкой и нейтральностью.

Мини-примеры ожидаемой интерпретации:
- "Я люблю тебя, дружище :)" => affection очень высокая, joy умеренно высокая, humor низкая или умеренная, surprise/confusion/aggression/disgust близки к нулю, dominant ближе к affection.
- "Да заткнись уже, ты бесишь" => aggression и tension высокие, joy и affection низкие.
- "Я украл твою лучшую игрушку. Ну же, отомсти мне" => aggression и tension должны быть заметными из-за провокации и подстрекательства, даже если прямого оскорбления мало.
- "Ну да, конечно, гениальный план" => sarcasm и contempt заметные, joy не должен быть главным.
- "Что вообще происходит, я ничего не понимаю?" => confusion и curiosity высокие, aggression низкая.

Формат JSON:
{
  "experts": [
    {
      "expert": "literal_semantics",
      "focus": "string",
      "scores": {
${emotionBlock}
      },
      "dominant": "joy",
      "confidence": 0.0,
      "explanation": "string"
    }
  ],
  "consensusExplanation": "string"
}

Сообщение пользователя:
${JSON.stringify(message)}
`;
}
