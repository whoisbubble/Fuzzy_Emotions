export function buildEmotionPrompt(message: string): string {
  return `
Ты анализируешь эмоциональный смысл сообщения пользователя для системы генерации мимики лица.

Верни СТРОГО JSON без markdown, без пояснений вне JSON.

Нужно выставить 5 экспертных оценок от 0 до 1:

1. humor — юмор, ирония, бытовое преувеличение, смешной оттенок.
2. aggression — злость, раздражение, нападение, грубость.
3. sadness — грусть, усталое сожаление, подавленность.
4. surprise — удивление, шок, внезапность.
5. confusion — непонимание, абсурдность, бессмысленность, странность сообщения.

Также верни:
- dominant: главная эмоция короткой строкой.
- confidence: уверенность анализа от 0 до 1.
- explanation: короткое объяснение на русском, не больше одного предложения.

Важно:
- Не оценивай эмоцию только по отдельным словам.
- Учитывай контекст, иронию и преувеличение.
- Фраза "какой ужас, моя собака много ест" скорее бытовая и смешная, а не настоящий страх.
- Если сообщение похоже на случайный набор символов, повышай confusion.
- Если сообщение матерное, но дружеское, не завышай aggression автоматически.

Формат:
{
  "scores": {
    "humor": 0.0,
    "aggression": 0.0,
    "sadness": 0.0,
    "surprise": 0.0,
    "confusion": 0.0
  },
  "dominant": "string",
  "confidence": 0.0,
  "explanation": "string"
}

Сообщение пользователя:
${JSON.stringify(message)}
`;
}